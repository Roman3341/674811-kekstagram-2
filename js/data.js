//Функция генерации массива объектов описания фото
const photos = function () {
  const photosArr = [];
  for (let i = 1; i < 26; i++) {
    const commentsArr = [];
    for (let j = 0; j < getRandomInt(0, 30); j++) {
      commentsArr.push({
        id: getRandomInt(0, 1000),
        avatar: `img/avatar-${ getRandomInt(1, 6) }.svg`,
        message: DESCRIPTION_MESSAGE[getRandomInt(0, DESCRIPTION_MESSAGE.length - 1)],
        name: NAMES[getRandomInt(0, NAMES.length - 1)]
      });
    }
    photosArr.push({
      id: i,
      url: `photos/${ i }.jpg`,
      description: PHOTO_DESCRIPTIONS[i - 1],
      likes: getRandomInt(15, 200),
      comments: commentsArr
    });
  }
  for (let i = photosArr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [photosArr[i], photosArr[j]] = [photosArr[j], photosArr[i]];
  }
  return photosArr;
};

photos();
